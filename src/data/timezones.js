export const TIMEZONE_LIST = Object.keys(TIMEZONE_COUNTRY_MAP).sort();

export function searchTimezones(query) {
  const q = (query || '').toLowerCase().trim();
  if (!q) return TIMEZONE_LIST.slice(0, 100).map(toOption);

  const results = TIMEZONE_LIST.filter((tz) => {
    const country = (TIMEZONE_COUNTRY_MAP[tz] || '').toLowerCase();
    return tz.toLowerCase().includes(q) || country.includes(q);
  });

  return results.slice(0, 100).map(toOption);
}

function toOption(value) {
  const country = TIMEZONE_COUNTRY_MAP[value] || '';
  return {
    text: { type: 'plain_text', text: `${value} (${country})`.slice(0, 75) },
    value,
  };
}
